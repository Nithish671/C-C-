#include <iostream>
#include <string>
using namespace std;

int main() {
    int myNum = 25;
    float myFloatNum = 6.25;
    double myDoubleNum = 11.10;
    char myLetter = 'S';
    bool myBoolean = false;
    string myString = "Gojo";

    cout << "int : " << myNum << "\n";
    cout << "float : " << myFloatNum << "\n";
    cout << "double : " << myDoubleNum << "\n";
    cout << "char : " << myLetter << "\n"; 
    cout << "boolean : " << myBoolean << "\n";
    cout << "string : " << myString << "\n";
}